module LessonOne

// How to bind a value to a variable.
let x = 3

// This will be immutable, you cannot reassign this variable.
// If you want the ability to mutate you will have to use the mutable keyword.

// Now we can mutate the variable.
let mutable y = 3

// How do we mutate the variable? We have to use the <- keyword.
y <- 4


